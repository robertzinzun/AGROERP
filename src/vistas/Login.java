    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        if(txtUser.getText().isEmpty()||txtPwd.getText().isEmpty())
            lblMsg.setText("!!Se deben informar los campos!!");
        else{
            u.setNombre(txtUser.getText());
            u.setClave(txtPwd.getText());
            if(udao.validar(u)){
                Principal p=new Principal(u);
                p.pack();
                p.setVisible(true);
                this.dispose();
            }
            else{
                lblMsg.setText("!!Datos de sesion incorrectos!!");
            }
        }
            
    }//GEN-LAST:event_btnEntrarActionPerformed

